// creating different variable for each type exercise

// string
let welcome: string = "Welcome in the Matrix. Follow the white rabbit...";
console.log(welcome);


// boolean 
let bearIsAwesome: boolean = true;
console.log(bearIsAwesome);

// integer or floating point number
let gravityConstant: number = 9.81;
console.log(gravityConstant);

// floating point number másképpen
let sin30Fok: number = 0.5000;
console.log(sin30Fok);

// assigning and creating a variable, (define its value)
let number: number = 36;

// mutate a variable, (redefine its value)
number = 13;

// Any típus
let force: any = 13;
force = false;
force = 'wavy';

// arithmetic assignment operators
let a: number = 13;
a = a + 27;
console.log(a); // prints 40

let b: number = 122;
b = b - 63;
console.log(b); // prints 59

let c: number = 333;
c = c *= 205;
console.log(c); // prints 68265

let d: number = 967;
d = d /= 12;
console.log(d); // prints 80.5833333333333

let g: number = 103;
g++;
console.log(g); // prints 104 - eggyel növelte

let i: number = 777;
i--;
console.log(i); // prints 776 - eggyel csökkentette


// nem mindegy hogy a változó előtt v mögött szerepel a ++ vagy -- 
let h: number = 1000;
console.log(h);
console.log(h++); // ez még nem adja hozzá láthatóan, csak amikor a változot kiiratom. 
console.log(h);
console.log(--h); // ez viszont egyből levonja, ha előtte van.







